//Tipos primitivos

//Tipo inteiro
int idade = 3;
float sal = 1925.54f;
char letra = 'G';
boolean casado = false;

//Type Cast
int idade = (int) 3;
float sal = (float) 1925.54;
char letra = (char) 'G';
boolean casado = (boolean) false; 

//by class (Wrapper Class)
Integer idade = new Integer(3);
Float sal = new Float(1925.54);
Character letra = new Character('G');
Boolean casado = new Boolean(false);